#include <bits/stdc++.h>
using namespace std;

class Solution {
public:
    long long largestSquareArea(vector<vector<int>>& bottomLeft,
                                vector<vector<int>>& topRight) {
        int n = bottomLeft.size();
        long long ans = 0;

        for (int i = 0; i < n; i++) {
            for (int j = i + 1; j < n; j++) {
                long long left   = max(bottomLeft[i][0], bottomLeft[j][0]);
                long long right  = min(topRight[i][0], topRight[j][0]);
                long long bottom = max(bottomLeft[i][1], bottomLeft[j][1]);
                long long top    = min(topRight[i][1], topRight[j][1]);

                long long width = right - left;
                long long height = top - bottom;

                if (width > 0 && height > 0) {
                    long long side = min(width, height);
                    ans = max(ans, side * side);
                }
            }
        }

        return ans;
    }
};

int main() {
    Solution sol;

    vector<vector<int>> bottomLeft = {{1,1},{2,2},{3,1}};
    vector<vector<int>> topRight   = {{3,3},{4,4},{6,6}};

    cout << sol.largestSquareArea(bottomLeft, topRight) << endl;

    return 0;
}
